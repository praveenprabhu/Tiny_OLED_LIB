/**
 *@brief Initilize SH1106
 */
void OLED_Init() {
	I2C_start(OLED_ADD);
	I2C_write(CMDS);
	for(int i = 0; i < sizeof(init_commands); i++) {
		I2C_write(init_commands[i]);
	}
	I2C_stop();
}	
	
/**
 * @brief Clear Display
 */
void OLED_Clear() {
	for (int p = 0 ; p < 8; p++) {
		I2C_start(OLED_ADD);
		I2C_write(CMDS);			
		I2C_write(0xB0 + p);
		I2C_write(0x02);
		I2C_write(0x10);
		I2C_stop();
		I2C_start(OLED_ADD);
		I2C_write(DTA);
		
			for (int i = 0 ; i < 128; i++) 
					I2C_write(0x00);
			I2C_stop();
	}
}


/**
 * @brief Prints a string at a specific pixel column and page row.
 * @param x    Starting column pixel (0 to 127)
 * @param page Target memory page row (0 to 7)
 * @param str  Null-terminated string to print
 */
void OLED_print(uint8_t x, uint8_t page, const char* str) {
    
    // Account for the required SH1106 2-pixel column offset hardware shift
    uint8_t actual_x = x + 2; 
    
    // 1. Position the hardware internal cursor
    I2C_start(OLED_ADD);
    I2C_write(CMDS); // Control byte: Command stream
    
    I2C_write(0xB0 + page);                 // Set Page Address
    I2C_write(0x00 + (actual_x & 0x0F));    // Lower 4 bits of column address
    I2C_write(0x10 + ((actual_x >> 4) & 0x0F)); // Upper 4 bits of column address
    I2C_stop();

    // 2. Stream character glyph data bytes
    I2C_start(OLED_ADD);
    I2C_write(DTA); // Control byte: Data stream

    while (*str) {
        uint8_t ascii_idx = *str - 32; // Map ASCII char to font array index

        // Stream the 5 vertical slices (columns) making up the character
        for (uint8_t i = 0; i < 5; i++) {
            I2C_write(CharMap[ascii_idx][i]);
        }
        
        // Send a 1-pixel blank vertical column space between characters
        I2C_write(0x00); 
        
        str++;
    }
    I2C_stop();
}

