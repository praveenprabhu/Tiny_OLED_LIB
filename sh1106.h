#include "i2c_tx.c"

#define OLED_ADD 		0x3c << 1	// Display i2c address, adjusted to 7 bits
#define CMDS			0x00        // Write multiple commands
#define	CMD				0x80		// Write single command
#define DTA				0x40        // Write data stream

/**
 *  Command Sequence Array for SH1106
 */
uint8_t init_commands[] = {
    0xAE,       // Display Off
    0x02, 0x10,   // Column addresses (Accounting for 1.3" panel offset)
    0x40,       // Start line 0
    0xB0,       // Page 0
    0x81, 0xCF,   // Set Contrast
    0xA1,       // Set Segment Remap (Horizontal Flip)
    0xC8,       // Set COM Scan Direction (Vertical Flip)
    0xA6,       // Normal Display Mode
    0xA8, 0x3F,   // Multiplex ratio 1/64
    0xD3, 0x00,   // No display offset
    0xD5, 0x80,   // Clock divide ratio
    0xD9, 0xF1,   // Pre-charge period
    0xDA, 0x12,   // COM pins hardware config
    0xDB, 0x40,   // VCOMH deselect level
    0x8D, 0x14,   // Charge Pump Enable 
    0xA4,       // Output follows RAM
    0xAF        // Display On
};

